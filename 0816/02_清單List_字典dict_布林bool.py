
# 清單

a = [1, 2, 3, 4, 5]
print(a)
print(len(a))
print(a[0])
print(a[4])
a[4] = 99
print(a)

print(a[0:2])
print(a[1:])
print(a[:3])
print(a[:-1])
print(a[:-2])

